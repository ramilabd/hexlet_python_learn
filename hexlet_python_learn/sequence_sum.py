def sequence_sum(begin, end):
    # Если начальное значение больше конечного — последовательность пустая
    if begin > end:
        return None

    # Базовый случай: если границы сравнялись, возвращаем само число
    if begin == end:
        return begin

    # Рекурсивный шаг: берем текущее число и прибавляем к нему
    # сумму оставшейся части последовательности
    return begin + sequence_sum(begin + 1, end)